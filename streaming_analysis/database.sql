CREATE DATABASE streaming;
USE streaming;

-- criando tabela de usuário
CREATE TABLE USER (
	ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(60),
    AGE VARCHAR(3),
    GENDER VARCHAR(10),
    EMAIL VARCHAR(50),
    COUNTRY CHAR(2)
);

-- criando tabela de títulos disponíveis para streaming na plataforma
CREATE TABLE CONTENT (
	ID INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(40),
    GENRE VARCHAR(15),
    STUDIO VARCHAR(25),
    AUDIENCE_SCORE VARCHAR(50),
    PROFITABILITY VARCHAR(20),
    RATE VARCHAR(5),
    WORLDWIDE_GROSS VARCHAR(10),
    YEAR CHAR(4)
);

-- criando tabela de stream por usuário
CREATE TABLE HISTORY (
	ID INT AUTO_INCREMENT PRIMARY KEY,
    USER_ID INT,
    CONTENT_ID INT,
    WATCHED_PERCENT VARCHAR(7),
    RATE CHAR(1),
    CREATED_TIME VARCHAR(40),
    FOREIGN KEY(USER_ID) REFERENCES USER(ID),
    FOREIGN KEY(CONTENT_ID) REFERENCES CONTENT(ID)
);

